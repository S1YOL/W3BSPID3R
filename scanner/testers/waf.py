# Generic malicious payload to trigger WAF block pages
_TRIGGER_PAYLOAD = "<script>alert(1)</script>../../etc/passwd' OR 1=1--"


class WAFTester(BaseTester):
    """Detects Web Application Firewall presence and identifies the product."""

    def __init__(self) -> None:
        super().__init__(name="WAF Detector")

    def run(self, pages: list[CrawledPage]) -> list[Finding]:
        self.findings.clear()
        self._params_tested = 0

        if not pages:
            return self.findings

        target_url = pages[0].url
        self._count_test()

        # Step 1: Get a clean baseline response
        try:
            baseline = http_utils.get(target_url)
        except Exception as exc:
            logger.debug("WAF detection baseline request failed: %s", exc)
            return self.findings

        # Step 2: Send a malicious request to trigger WAF
        try:
            sep = "&" if "?" in target_url else "?"
            trigger_url = f"{target_url}{sep}waftest={_TRIGGER_PAYLOAD}"
            trigger_resp = http_utils.get(trigger_url)
        except Exception as exc:
            logger.debug("WAF trigger request failed: %s", exc)
            trigger_resp = None

        # Step 3: Check both responses for WAF signatures
        detected_wafs = set()

        for resp in [baseline, trigger_resp]:
            if resp is None:
                continue
            for sig in _WAF_SIGNATURES:
                waf_name = sig["name"]
                if waf_name in detected_wafs:
                    continue

                # Check headers
                try:
                    if sig["headers"](resp.headers):
                        detected_wafs.add(waf_name)
                        continue
                except Exception:
                    pass

                # Check body patterns
                body_lower = resp.text.lower()
                for pattern in sig["body"]:
                    if re.search(pattern, body_lower, re.IGNORECASE | re.DOTALL):
                        detected_wafs.add(waf_name)
                        break

        # Step 4: Check for generic WAF behaviour (status code change on malicious input)
        generic_waf = False
        if trigger_resp is not None:
            if (baseline.status_code == 200
                    and trigger_resp.status_code in (403, 406, 429, 501)):
                generic_waf = True

        # Report findings
        if detected_wafs:
            waf_list = ", ".join(sorted(detected_wafs))
            finding = Finding(
                vuln_type=VulnType.WAF_DETECTED,
                severity=Severity.LOW,
                url=target_url,
                parameter="(header/response analysis)",
                method="GET",
                payload="(passive fingerprinting + malicious trigger)",
                evidence=(
                    f"Web Application Firewall detected: {waf_list}. "
                    f"Baseline status: {baseline.status_code}, "
                    f"Trigger status: {trigger_resp.status_code if trigger_resp else 'N/A'}. "
                    f"WAF may block attack payloads — other scan results could have false negatives."
                ),
                remediation=(
                    f"WAF detected: {waf_list}. This is an informational finding. "
                    "WAF presence is a positive defence-in-depth measure. Ensure the WAF "
                    "rules are up-to-date and that the application does not solely rely on "
                    "the WAF for security — all input validation should also be done server-side."
                ),
            )
            self._log_finding(finding)
        elif generic_waf:
            finding = Finding(
                vuln_type=VulnType.WAF_DETECTED,
                severity=Severity.LOW,
                url=target_url,
                parameter="(behavioural analysis)",
                method="GET",
                payload=_TRIGGER_PAYLOAD,
                evidence=(
                    f"Generic WAF behaviour detected: baseline returned {baseline.status_code} "
                    f"but malicious payload returned {trigger_resp.status_code}. "
                    f"Could not identify specific WAF product. "
                    f"Other scan results may have false negatives due to WAF blocking."
                ),
                remediation=(
                    "A WAF or request filtering mechanism is active. This is generally positive. "
                    "Ensure the application also validates input server-side and doesn't rely "
                    "solely on the WAF. Keep WAF rules updated."
                ),
            )
            self._log_finding(finding)

        return self.findings
