from __future__ import annotations
"""
scanner/testers/waf.py
-----------------------
Web Application Firewall (WAF) detection module.

Security concept:
  Knowing whether a WAF is present is critical for pentesters because:
  1. Payloads may be blocked, causing false negatives in other testers
  2. The WAF vendor/type informs bypass strategies
  3. The presence of a WAF is itself a security finding (defence-in-depth)

Detection strategy:
  - Send a benign request and a known-malicious request, compare responses
  - Check response headers for WAF fingerprints (Server, X-*, Set-Cookie)
  - Check response body for WAF block pages
  - Identify specific WAF products by their unique signatures
"""

import logging
import re

from scanner.crawler import CrawledPage
from scanner.reporting.models import Finding, Severity, VulnType
from scanner.testers.base import BaseTester
from scanner.utils import http as http_utils

logger = logging.getLogger(__name__)


